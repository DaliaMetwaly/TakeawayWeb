        public ActionResult EditAddress(ChangePasswordVM changePasswordVM)
        {
            ModelState.Remove("ChangePasswordVM.OldPassword");
            ModelState.Remove("ChangePasswordVM.NewPassword");
            ModelState.Remove("ChangePasswordVM.ConfirmPassword");
            ModelState.Remove("ChangePasswordVM.OldEmail");
            ModelState.Remove("ChangePasswordVM.NewEmail");

            bool status = false;
            if (ModelState.IsValid)
            {
                //Update Table UserData
                if (changePasswordVM.UserDataList.Count > 0)
                {
                    string loggedUsr = User.Identity.GetUserId().ToString();
                    db.UserDatas.RemoveRange(db.UserDatas.Where(x => x.UserID == loggedUsr).ToList());
                    List <UserData> userData = new List <UserData>();
                    userData = changePasswordVM.UserDataList;
                    foreach (var userDataItem in userData)
                    {
                        UserData singleUserData = new UserData();
                        singleUserData.UserID = User.Identity.GetUserId().ToString();
                        singleUserData.Address = userDataItem.Address;
                        singleUserData.Region_id = userDataItem.Region_id;
                        singleUserData.MapLatitude = userDataItem.MapLatitude;
                        singleUserData.MapLongitude = userDataItem.MapLongitude;
                        singleUserData.IsActive = true;
                        db.UserDatas.Add(singleUserData);
                    }
                }


                db.SaveChanges();
                status = true;

            }
            else
            {
                status = false;
            }
            return new JsonResult { Data = new { status = status } };
        }
